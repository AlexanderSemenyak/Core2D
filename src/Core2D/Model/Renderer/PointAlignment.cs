
namespace Core2D.Renderer
{
    /// <summary>
    /// Specifies point alignment.
    /// </summary>
    public enum PointAlignment
    {
        /// <summary>
        /// Point is not aligned.
        /// </summary>
        None,

        /// <summary>
        /// Point is aligned to the left horizontally.
        /// </summary>
        Left,

        /// <summary>
        /// Point is aligned to the right horizontally.
        /// </summary>
        Right,

        /// <summary>
        /// Point is aligned to the top vertically.
        /// </summary>
        Top,

        /// <summary>
        /// Point is aligned to the bottom vertically.
        /// </summary>
        Bottom
    }
}
